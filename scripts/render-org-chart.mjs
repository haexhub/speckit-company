import { readFile, readdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { parse as parseYaml } from "yaml";

const STATUS_STYLES = {
  active: "fill:#90EE90,stroke:#2E8B57,color:#000",
  pending_retire: "fill:#FFD700,stroke:#B8860B,color:#000",
  retired: "fill:#A9A9A9,stroke:#696969,color:#000",
};

/**
 * Render the reports-to graph as a Mermaid flowchart string.
 *
 * Agents with status='retired' are dropped — they aren't part of the live org.
 *
 * @param {Array<{role: string, reports_to: string|null, status: string, model?: string}>} agents
 * @returns {string} a Mermaid `flowchart TD` block (no fences)
 */
export function renderOrgChart(agents) {
  const visible = agents.filter((a) => a.status !== "retired");
  const lines = ["flowchart TD"];

  for (const a of visible) {
    const label = formatLabel(a);
    lines.push(`  ${a.role}["${label}"]:::${a.status ?? "active"}`);
  }

  for (const a of visible) {
    if (a.reports_to && visible.some((x) => x.role === a.reports_to)) {
      lines.push(`  ${a.reports_to} --> ${a.role}`);
    }
  }

  // Style classes
  const usedStatuses = new Set(visible.map((a) => a.status ?? "active"));
  for (const status of usedStatuses) {
    const style = STATUS_STYLES[status] ?? STATUS_STYLES.active;
    lines.push(`  classDef ${status} ${style}`);
  }

  return lines.join("\n");
}

function formatLabel(agent) {
  const lines = [agent.role];
  if (agent.model) lines.push(agent.model);
  return lines.join("<br/>");
}

/**
 * Read agents from <orgDir>/agents/, filter retired, render full markdown
 * with mermaid fences. Returns a string ready to write to org-chart.md.
 */
export async function renderOrgChartFromDir(orgDir) {
  const agents = await loadAgents(orgDir);
  const mermaid = renderOrgChart(agents);
  return `# Organization Chart\n\nGenerated by speckit-company. Do not edit by hand — re-run \`/speckit-company.org-chart\` instead.\n\n\`\`\`mermaid\n${mermaid}\n\`\`\`\n`;
}

async function loadAgents(orgDir) {
  const agentsDir = path.join(orgDir, "agents");
  const files = (await readdir(agentsDir)).filter((f) => f.endsWith(".md"));
  const agents = [];
  for (const file of files) {
    const raw = await readFile(path.join(agentsDir, file), "utf8");
    const fm = parseFrontmatter(raw);
    if (fm) agents.push(fm);
  }
  return agents;
}

function parseFrontmatter(raw) {
  const match = raw.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return null;
  return parseYaml(match[1]);
}

// CLI entry
if (process.argv[1] && import.meta.url === `file://${process.argv[1]}`) {
  const orgDir = process.argv[2];
  if (!orgDir) {
    console.error("Usage: render-org-chart.mjs <org-dir>");
    process.exit(2);
  }
  const md = await renderOrgChartFromDir(orgDir);
  process.stdout.write(md);
}
